ns = Namespace("http://oeg.fi.upm.es/def/people#")
individuals = []

def get_all_subclasses(cls, graph):
    subclasses = set()
    for subclass in graph.subjects(RDFS.subClassOf, cls):
        subclasses.add(subclass)
        subclasses.update(get_all_subclasses(subclass, graph))
    return subclasses

